<h3 align="center">Системный блок и блок питания. Система охлаждения.</h3>

<p align="justify"><strong>Системный блок</strong>, если говорить утрировано, представляет собой шасси, в котором <strong>объединены все аппаратные компоненты обеспечивающие работу компьютера</strong>.</p>
<div id="attachment_10313" style="width: 470px" class="wp-caption aligncenter"><img src="siis_block.jpg" alt="Системный блок" width="460" height="490" class="size-full wp-image-10313" srcset="http://vchemraznica.ru/wp-content/uploads/2017/02/siis_block-282x300.jpg 282w, http://vchemraznica.ru/wp-content/uploads/2017/02/siis_block.jpg 460w" sizes="(max-width: 460px) 100vw, 460px" /><p class="wp-caption-text">Системный блок</p></div>
<p align="justify">На него возложены функции создания вычислительной техники с дальнейшей возможностью расширение вычислительной системы.</p>
<p align="justify">Содержимое каждого системного блока может различаться, чаще всего начинка зависит от функциональных задач, которые планируется реализовывать с помощью этой техники. Но основными составляющими системного блока являются:</p>
<ul>
<li>Вычислительный блок, который объединяет материнскую плату, центральный процессор, расположенный на ней.</li>
<li>Звуковая, сетевая и видео карта, которые крепятся к материнской плате специальными крепежными элементами.</li>
<li>Винчестер, монтаж которого проводится благодаря специальному шлейфу.</li>
<li>Планки оперативной памяти, которые помогают обеспечить быструю работу устройства.</li>
<li>Блок питания.</li>
</ul>
<p align="justify">Конструкция системного блока подразумевает наличие разъемов и выходов для присоединения к нему различных первичных устройств и необходимых для работы компонентов компьютера. Обязательно в комплектации персонального компьютера кроме самого системника входят мышка, клавиатура и монитор.</p>
<p align="justify">Также внутри системного блока, по желанию пользователя, могут располагаться приводы для чтения информации с различных носителей и прочие периферийные устройства, которые нужны для качественной работы.</p>
<p align="justify"><img src="vnut66.jpg" alt="Внутри системного блока" width="660" height="490" class="aligncenter size-full wp-image-10314" srcset="http://vchemraznica.ru/wp-content/uploads/2017/02/vnut66-300x223.jpg 300w, http://vchemraznica.ru/wp-content/uploads/2017/02/vnut66.jpg 660w" sizes="(max-width: 660px) 100vw, 660px" /></p>
<p align="justify">По своей сути, системный блок – это <strong>коробка, в которой объединены все наиболее важные элементы</strong>, что отвечают за работу всего устройства в целом.</p>
<p align="justify">Фронтальная панель корпуса дополнительно имеет функциональные элементы:</p>
<ul>
<li>Кнопки включения и выключения.</li>
<li>Различные индикаторы состояния, например, питания.</li>
<li>Гнезда для подключения наушников, микрофона и колонок.</li>
<li>Интерфейсы, что отвечают за передачу данных, например, USB, FireWire.</li>
</ul>
<p align="justify">Как стало понятно, что в принципе сравнивать процессор и системный блок вообще нет смысла – этой совершенно разные вещи. Их объединяет одно – они используются для создания высокоскоростной производительной вычислительной системы.</p>

</br></br>
<p align="justify"><strong><img src="blok-p.jpg" alt="блок питания компьютера" title="Блок питания компьютера" width="128" height="128" hspace="15" vspace="4" align="left" />Блок питания компьютера</strong> – это механическое или электронное  устройство. </p>
  <p align="justify">Блок питания формирует напряжение, получаемое от электрической  сети, которое необходимо различным типам аппаратуры.</p>
 <p align="justify">
В Российской Федерации переменный ток 
  


электросети 220 В  частотой 50 Гц, преобразуется в нужное количество постоянного тока. В иных же  странах используются другие напряжения и частоты.</p>

 <p align="justify">
  Для примера сейчас рассмотрим <em>трансформаторный блок питания</em>,  <span class="smotri">признанный «классическим»</span>. Такой блок питания включает в себя понижающий трансформатор,  выпрямителя, который преобразует переменный ток в постоянный. После  выпрямителей, для сглаживания пульсации тока, устанавливается специальный  фильтр – это конденсатор большой емкости. </p>
 <p align="justify">Иногда используются другие схемы трансформаторных  блоков питания, например, это может быть применение выпрямителей с двойным  напряжением, стабилизаторы тока, фильтры высокочастотных помех и другое. </p>
 <p align="justify">
  Главное преимущество трансформаторного <strong>блока питания</strong> – это его  простота, надежность, низкая стоимость и высокая доступность элементов – легкий  ремонт и замена деталей. </p>
 <p align="justify">
  Недостатками трансформаторных блоков, являются – большой вес,  невысокий коэффициент полезного действия и высокое содержание металлов. </p>
 <p align="justify">
  <strong>Трансформаторные блоки питания</strong> очень часто сравнивают с  импульсными. Импульсные блоки питания имеют небольшой вес и обладают  стабильностью и гарантируют бесперебойную и долгую работу. Такие блоки питания  чаще всего используются на складах, фабриках, цехах и т.д.</p>

