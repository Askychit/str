		<h2 itemprop="name">
							Системы охлаждения компьютера					</h2>
							</div>
 
<p align="justify">Компьютер - это устройство, cсостоящие из электронных компонентов, которые являются проводниками. При прохождении  по ним электрического тока выделяется тепло. Об этом нам говорит физический закон.
</p>

<p align="justify"><img src="system-holod-1.jpg" width="400px" alt="Произведение народных умельцев" title="Система охлаждения" />
</p>

 
<p align="justify">Греется компьютер неравномерно - некоторые части нагреваются сильно, какие-то остаются холодными. И все бы хорошо, только разогрев радиоэлементов до высокой температуры не очень полезен и может стать причиной выхода их из строя. Именно поэтому были придуманы системы охлаждения компьютера.
</p>


<p align="justify">В те далеки времена, когда люди ездили на лошадиных повозках, а мужики носили лапти о системах охлаждения никто не думал, они просто были не нужны. Другое дело сейчас, в настоящее время системы охлаждения активно развиваются. Как охладить компьютер? Этим вопросом задаются многие фирмы и просто рядовые пользователи. Кто просто думает, ну а кто изучает проблему детально и своими руками мастерит новые конструкции. Такие исследования порой обходятся не дешево, например, в пару сгоревших системных блоков. Но что сделаешь? Это издержки.
</p>

<p align="justify">Народ у нас талантливый, изобретательный. Глядишь на новую разработку очередного умельца и думаешь: "А что? Неплохо придумано. И старый холодильник "Апшерон" пригодился. "
</p>


<p align="justify">Итак, системы охлаждения разделяют на пассивные и активные.
</p>

<p align="justify">Пассивные - без движущих механизмов и питания.
</p>



<p align="justify"><h3>1.Радиаторы</h3>
</p>

<img src="system-holod-2.jpg" class="pull-right" alt="Пассивная система охлаждения" title="Радиатор" />

<p align="justify">И снова, здравствуй, физика. Существует такой вид теплового обмена, как теплопередача. Радиатор при контакте с греющимся элементом забирает часть тепла, нагреваясь сам. Соответственно, чем больше радиатор, тем больше он забирает тепла. Также большую роль играет теплопроводность материала, из которого изготовлен радиатор. Наибольшей теплопроводностью из металлов обладает алюминий, еще лучший показатель у меди и самым теплопроводным металлом считается серебро. Однако, исходя из стоимости металлов  можно сделать вывод, что радиаторы из серебра будут не всем по карману.
</p>

<p align="justify">Радиаторы, используются самостоятельно, так и в комплексе с воздушным видом охлаждения (вентиляторами), или с другими видами.
</p>

<p align="justify">Неплохим дополнением к радиатору является герметичная трубка, заполненная жидкостью, но не полностью, а лишь частично. Эта трубка соединяет между собой два радиатора. Один из радиаторов нагревается и жидкость начинает испаряться с этой части трубки, нагревая равномерно всю трубку.  Пар конденсируется на другом (холодном конце) трубки. Таким образом, теплообмен происходит более быстро. 
</p>

<p align="justify">Преимущества пассивных систем: 
</br>-бесшумность
</br>-надежность
</br>-безопасность 
</p>

<p align="justify">Недостатки:
</br>-низкая эффективность
</p>

<p align="justify"><h3>2. Воздушные системы</h3></p>

<img src="system-holod-3.jpg" alt="Кулер с подсветкой" class="pull-left" title="Система охаждения" />
<p align="justify">
Это наиболее распространенный вид охлаждения. Используется воздушное охлаждение, часто вместе с радиаторами. В системном блоке необходимо правильно организовать воздушный поток, таким образом, чтобы горячий воздух выдувался из блока, а холодный поступал в блок и охлаждал наиболее греющиеся устройства: процессор, видеокарту, жесткий диск, оперативную память и др. 
</p>

<p align="justify">Воздушную систему улучшают: увеличевая скорости вращения кулеров, увеличением диаметра  вентиляторов  и наращиванием их количества. В корпусах обычно закладывают специальные посадочные места для дополнительных кулеров.
Одной из важных характеристик является также уровень шума. Тихим считается кулер с шумом в 15 - 20 дБ. Естественно, чем больше кулеров и чем быстрее их скорость, тем выше уровень шума. Более высокоэффективные кулеры, как правило, более шумные. 
</p>

<p align="justify"> Вентиляторы также могут быть оснащены светодиодной подсветкой разных цветов. 
</p>

<p align="justify">Преимущества: 
</br>-высокая эффективность
</br>-надежность
</br>-безопасность 
</p>

<p align="justify">Недостатки:
</br>-высокий уровень шума
</p>

<p align="justify"><h3>3.Жидкостные системы</h3></p>

<img src="system-holod-4.gif" alt="Схема жидкостной системы" class="pull-right" title="Жидкостная система" />

<p align="justify">Не так давно жидкостная система была новшеством на рынке систем охлаждения, однако сейчас она твердо заняла свое место. 
</p>

<p align="justify">Жидкости являются лучшим проводником тепла, чем воздухом, поэтому жидкостные системы более эффективные.
Основными элементами жидкостной системы охлаждения являются теплообменник, который передает тепло от нагреваемого элемента жидкости. После этого жидкость по средствам трубок доставляется в радиатор, в котором проходит процесс охлаждения жидкости.  Для того,  чтобы жидкость циркулировал по системе используется насос. Помпа постоянно гоняет воду по системе, иначе эффективность системы упадет в разы.  Насосы бывают двух видов: погружаемые - должны находится в резервуаре, заполненном жидкостью и наружные. Если используется погружаемый насос, то необходимо к нему изготовить резервуар или купить готовый.
</p>

<p align="justify">Также оснащаются светодиодной подсветкой, что придает дизайну компьютера особый вид.
</p>

<p align="justify">Преимущества: 
</br>-высокая эффективность
</br>-низкий уровень шума
</br>-небольшие габариты теплообменников
</p>

<p align="justify">Недостатки:
</br>-опасность при разгерметизации системы
</br>-высокая стоимость
</br>-неадаптированность любого корпуса к такой системе
</p>

<p align="justify"><h3>4. Погружные системы охлаждения</h3></p>
<p align="justify">
<img src="system-holod-5.jpg" alt="Погружная система" title="Погружная система" />
</p>
<p align="justify">Одной из новых систем охлаждения, которая пока еще не находится в серийном производстве. 
Идея такова : "Если жидкость лучший проводник тепла, чем воздух, то необходимо компьютер погрузить в  жидкость полностью ."
</p>

<p align="justify">Для реализации такой идеи необходимо изготовить аквариум для компьютера, установить там материнскую карту, видеокарту, так чтобы к ним можно было подключится с внешней стороны резервуара. После сборки компьютера, залить  в аквариум жидкость, которая не проводит электрический ток. 
</p>

<p align="justify">Преимущества: 
</br>-высокая эффективность
</br>-низкий уровень шума
</p>

<p align="justify">Недостатки:
</br>-опасность залить все вокруг маслом при разгерметизации аквариума
</br>-большие габариты данной системы
</br>-неудобство при замене устройств в компьютере
</p>

<p align="justify"><h3>5. Экстремальные системы охлаждения</h3>
</p>

<p align="justify">
<img src="system-holod-6.jpg" alt="Экстремальная система охлаждения" title="Экстремальная система охлаждения" />
</p>
<p align="justify">Используются любителями, занимающимися разгоном процессора (при разгоне очень сильно греется процессор). Рядовым пользователям такие системы конечно ни к чему. Тут используются криогенные камеры, охлаждение азотом, охлаждение с применением фреона и другие методы. 
</p>  

  
  
  
  
  
