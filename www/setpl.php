<p align="justify">
	&nbsp; А сейчас еще раз посмотрите на фото, на котором крупно изображены разъемы сетевых карт компьютера. На правом изображении присутствуют всего четыре контактные площадки: первая, вторая, третья, следующие две - пропущены и дальше... какая? Правильно - шестая! :)</p>
<p align="justify">
	&nbsp;&nbsp;Когда же используются все восемь площадок? В сетях со скоростью передачи в один гибабит в секунду (и выше). Вот там все проводники сетевого кабеля используются на полную катушку :)</p>
<p align="justify">
	&nbsp; Так, что-то мы с Вами (вернее я один :)) &quot;урулил&quot; в сторону от главной темы. Какие же еще бывают сетевые карты? Давайте рассмотрим внешний адаптер для ноутбука на основе стандарта PCMCIA. Это - внешняя плата расширения, которая вставляется в соответствующий слот.</p>
<p align="justify">
	&nbsp;&nbsp;Расшифровывается &laquo;PCMCIA&raquo; как&nbsp;Personal Computer Memory Card International Association (международная ассоциация компьютерных карт памяти). Изначально стандарт разрабатывался для карт расширения памяти. Через некоторое время спецификация была расширена и стало возможным использование &laquo;PCMCIA&raquo; для подключения различных периферийных устройств. Как правило, через него подключают сетевые карты, модемы или же жёсткие диски. &nbsp;</p>
<p align="justify">
	&nbsp; Представьте неприятную картину: у Вашего ноутбука (три раза &quot;тьфу&quot; его налево) вышла из строя встроенная в него &nbsp;карта. Что делать? Решение - на фото ниже:</p>
<div align="center">
	<img itemprop="image" alt="PCMCIA адаптер" src="http://sebeadmin.ru/media/nic7.jpg" /></div>
<p align="justify">
	&nbsp; Есть, правда, и другие решения, которые подходят уже не только для мобильных компьютеров, но и для стационарных. Это - USB сетевые карты.</p>
<p align="justify">
	&nbsp; Они могут быть выполнены по разному, но принцип их работы от этого не меняется. Вот, к примеру, два таких устройства на фото ниже:</p>
<div align="center">
	<img itemprop="image" alt="USB сетевой адаптер" src="http://sebeadmin.ru/media/nic8.jpg" style="width: 350px; height: 209px; " /><br />
	&nbsp;</div>
<p align="justify">
	&nbsp; Или даже вот так, больше похоже на флеш накопитель :)</p>
<div align="center">
	<img itemprop="image" alt="USB сеть" src="http://sebeadmin.ru/media/nic9.jpg" style="width: 350px; height: 350px; " /></div>
<p align="justify">
	&nbsp; На этом собрался было закончить статью, но... передумал! :) Хотел еще рассказать Вам о такой разновидности внешних сетевых карт, как серверные сетевые карты, которые используются в высокопроизводительных системах и имеют более продвинутые (по сравнению с обычными адаптерами) возможности по работе с сетью.</p>
<p align="justify">
	&nbsp;&nbsp;Как правило, они имеют стандартный интерфейс подключения - PCI (или его расширенную версию - PCI-X). Вот, к примеру, серверная сетевая карта &laquo;<strong>D-Link&nbsp;DFE-580TX</strong>&raquo;. &nbsp;</p>
<div align="center">
	<img itemprop="image" alt="Серверная сетевая карта" src="http://sebeadmin.ru/media/nic10.jpg" style="width: 420px; height: 259px; " /><br />
	&nbsp;</div>
<p align="justify">
	&nbsp; Как видите, это, по сути - четыре сетевых адаптера, объединенные &nbsp;в одном физическом устройстве. Каждый из четырех сетевых портов (карт) имеет свой собственный MAC адрес (уникальный 12-ти значный физический идентификатор любой карты или другого сетевого устройства). В то же время, всей группе портов можно присвоить <strong>один</strong> логический идентификатор (IP адрес). Для операционной системы группа таких карт выглядит, как одна виртуальная карта.</p>
<p align="justify">
	<strong>&nbsp; Примечание</strong>: MAC (Media Access Control - управление доступом к среде) адрес также часто называют физическим или аппаратным адресом (Hardware Address). Например: MAC адрес моего сетевого адаптера на работе равен 00-1B-11-B3-C8-82. В сети не может быть двух одинаковых аппаратных адресов. Узнать его можно, введя в командной строке: <strong>ipconfig /all</strong> или такой замечательной командой, использующей одноименную утилиту, как <strong>getmac</strong>. Getmac в очень удобной и наглядной форме покажет Вам все MAC адреса всех сетевых устройств, установленных в компьютере.</p>
<p align="justify">
	&nbsp; Продолжим. Объединение нескольких карт в одну становится возможным при использовании технологии &laquo;Port Aggregation&raquo; (агрегирование или - объединение портов). Объединение портов означает объединение нескольких сегментов сети в один, обладающий большей производительностью. Когда несколько сетевых портов образуют один виртуальный, то его пропускная способность (теоретически) приравнивается к производительности отдельного порта, умноженную на их количество.</p>
<p align="justify">
	&nbsp; Серверные сетевые карты могут работать в двух основных режимах. Давайте рассмотрим их подробнее. С помощью программного обеспечения, идущего в комплекте с картами такого класса, можно сконфигурировать каждый порт как &quot;активный&quot; (режим балансировки нагрузки - load balancing) или зарезервировать любые порты для обеспечения отказоустойчивости (режим восстановления).</p>
<p align="justify">
	&nbsp; Режим разделения (распределения) нагрузки сети равномерно пропускает сетевой трафик (поток данных) через активные сегменты, снижая общую нагрузку на адаптер, а режим восстановления (в случае физического обрыва связи) обеспечивает бесперебойную связь между сетевой картой и сетью.&nbsp;</p>
<p align="justify">
	&nbsp; Чем же еще хороша серверная сетевая карта компьютера? В зависимости от своей &quot;навернутости&quot; :) она может реализовывать вычислительные функции (подсчёт и генерацию контрольных сумм кадров данных, передающихся по сети) аппаратно, не нагружая дополнительно центральный процессор компьютера.</p>
<p align="justify">
	&nbsp; На таких адаптерах устанавливаются специализированные БИС (Большие Интегральные Схемы), которые берут на себя значительную часть работы &nbsp;(обнаружение столкновений, сборка-разборка пакетов данных, проверка контрольных сумм кадров и повторная передача испорченных пакетов). Таким образом, как мы уже говорили, снимается значительная часть нагрузки с процессора, которому в серверной системе и без того есть чем заняться :)&nbsp;</p>
<p align="justify">
	&nbsp; Более того, на дорогие серверные сетевые карты устанавливают собственный процессор. Подобные карты показывают очень хорошие показатели в работе, поскольку могут эффективно справляться даже с большой нагрузкой. Наличие собственного процессора позволяет устанавливать на них до одного мегабайта оперативной памяти. А это уже переводит эти изделия из разряда просто сетевых карт в разряд коммуникационных сетевых процессоров.</p>
<p align="justify">
	&nbsp; Также нельзя не отметить такую полезную функцию, как самовосстанавливающиеся драйвера подобных устройств. Что это такое? К примеру, после сбоя в сети адаптер может самостоятельно принять решение о перезапуске драйвера сетевой карты, включить проверку целостности сетевого соединения или даже принудительно отключить вышедший из строя порт.&nbsp;</p>


