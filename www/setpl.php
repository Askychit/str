<h3 align="center">Сетевая плата.</h3>

<p align="justify">
	&nbsp; Затронем такую тему, как сетевая карта нашего компьютера. Начнем с того, что сетевые карты бывают разные и могут отличаться, как по спектру решаемых задач, так и по форм-фактору (внешнему виду). Сетевую плату также часто называют (эзернет (Ethernet) контроллером, сетевым или NIC (Network Interface Card) адаптером).&nbsp;</p>
<p align="justify">
	&nbsp;&nbsp;Прежде всего давайте разделим сетевые карты на две большие группы:</p>
<ul>
	<li>
		Внешние сетевые карты</li>
	<li>
		Встроенные или интегрированные (onboard)</li>
</ul>
<p align="justify">
	<br />
	&nbsp; Начнем - с внешних. Из самого названия следует, что сетевые карты данного типа устанавливаются в системный блок компьютера дополнительно (отдельной платой расширения) или же, как другое внешнее устройство.</p>
<p align="justify">
	&nbsp; Для начала поговорим о PCI сетевых картах. Аббревиатура расшифровывается как (Peripheral Component Interconnect) - взаимосвязь периферийных компонентов или - шина ввода-вывода для подключения периферийных устройств к материнской плате компьютера. Называются эти карты так по той причине, что устанавливаются в один из PCI слотов (разъемов). Вот в такие, собственно:</p>
<div align="center">
	<img itemprop="image" alt="PCI разъемы для сетевой карты" src="nic3.jpg" style="width: 350px; height: 359px; " /><br />
	&nbsp;</div>
<p align="justify">
	&nbsp;&nbsp;Сам по себе PCI интерфейс обладает пиковой пропускной способностью для 32-разрядного варианта, работающего на частоте 33,33 МГц в 133 Мбайт/с, потребляемое&nbsp;напряжение разъема 3,3 или 5V. &nbsp;Служит для установки в компьютер дополнительных плат расширения (старых видеокарт, модемов, сетевых адаптеров, TV тюнеров различных плат видеозахвата и конвертации видео и т.д).</p>
<p align="justify">
	&nbsp; Итак, какие же сетевые карты туда устанавливаются? А вот такие, самые обычные &nbsp;долларов за пять-шесть:</p>
<div align="center">
	<img itemprop="image" alt="Сетевая карта компьютера" src="nic1.jpg" style="width: 350px; height: 332px; " /></div>
<p align="justify">
	&nbsp; Бывают адаптеры и другой разновидности - Wi-Fi (для организации беспроводных сетей).</p>
<div align="center">
	<img itemprop="image" alt="Wi-Fi сетевая карта компьютера" src="nic2.jpg" style="width: 350px; height: 229px; " /><br />
	&nbsp;</div>
<p align="justify">
	&nbsp;&nbsp;Как видите, интерфейс подключения один (PCI), а принцип работы - разный.</p>
<p align="justify">
	&nbsp; Сейчас, в связи с постепенным &quot;отмиранием&quot; данного интерфейса выпускаются сетевые карты форм фактора &laquo;Pci Express 1X&raquo;.</p>
<div align="center">
	<img itemprop="image" alt="PCI Express 1x сетевая карта" src="nic11.jpg" style="width: 350px; height: 338px; " title="Сетевая карта компьютера" /></div>
<p align="justify">
	&nbsp; Это что касается внешних сетевых карт. Есть еще встроенные (интегрированные в материнскую плату) карты. Определить наличие встроенной сетевой можно посмотрев на тыльную стенку системного блока.</p>
<div align="center">
	<img itemprop="image" alt="Встроенная сетевая карта" src="http://sebeadmin.ru/media/nic4.jpg" title="Сетевая карта компьютера" /><br />
	&nbsp;</div>
<p align="justify">
	&nbsp; Здесь мы визуально можем наблюдать выход интегрированной сетевой карты. Рядом с разъёмом для витой пары устанавливают один или несколько информационных светодиодов, по которым можно сориентироваться о наличии подключения и общей активности сети.&nbsp;</p>
<p align="justify">
	&nbsp;&nbsp;К слову, по этим светодиодам можно <strong>косвенно</strong> составить представление относительно работоспособности устройства. Поясню свою мысль: когда компьютер включен и сетевой кабель (витая пара) подключен к карте светодиод на ней мигает, что называется, - в такт с приемом (передачей) информационных пакетов данных адаптером в сеть.</p>
<p align="justify">
	&nbsp;&nbsp;При нерабочем сетевом адаптере поведение индикаторов может быть следующее:</p>
<ol>
	<li>
		Не один из светодиодов не светится вообще</li>
	<li>
		Светодиод постоянно &quot;горит&quot; (не мигая)</li>
	<li>
		Индикатор мигает, но абсолютно однообразно. Период и амплитуда этого &quot;мигания&quot; одинаковы на протяжении всего времени</li>
</ol>
<br />
<p align="justify">
	&nbsp;&nbsp;Так что - примечайте подобные моменты. В деталях - наше все! :)</p>
<p align="justify">
	&nbsp; Если пойти дальше и вскрыть корпус, то внутри (недалеко от разъема) можно обнаружить распаяный на материнской плате чип, который и реализует все функции сетевого адаптера.</p>
<div align="center">
	<img itemprop="image" alt="Встроенный сетевой адаптер" src="http://sebeadmin.ru/media/nic5.jpg" style="width: 350px; height: 266px; " /></div>
<p align="justify">
	&nbsp; По маркировке видим, что это - микросхема &nbsp;RTL (от фирмы Realtek) под номером 8211BL.</p>
<p align="justify">
	&nbsp;&nbsp;<strong>Примечани</strong>е: встроенные решения, к сожалению, не отличаются надежностью. В нашей организации, к примеру, выходы из строя интегрированных сетевых карт случаются регулярно. Не могу сказать, что часто, но &nbsp;- стабильно. К слову, у моего (купленного пол года назад) рабочего компьютера буквально на днях сгорела сетевая карта, что лишний раз укрепило мое мнение о ненадежности интегрированных компонентов. Пришлось поставить внешнюю.&nbsp;</p>
<p align="justify">
	&nbsp; Хочу, чтобы Вы внимательно присмотрелись к следующему фото:</p>
<div align="center">
	<img itemprop="image" alt="Разъем сетевой карты" src="http://sebeadmin.ru/media/nic6.JPG" style="width: 460px; height: 147px; " /><br />
	&nbsp;</div>
<p align="justify">
	&nbsp; Здесь мы заглядываем внутрь разъема сетевой карты. Замечаете разницу? В одном разъеме (на фото - справа) четыре контактных площадки, а в другом (слева) - восемь. Причем обе карты рассчитаны на скорость передачи по сети в 100 мегабит в секунду.</p>
<p align="justify">
	&nbsp; В чем же здесь подвох? А он, по любому, здесь присутствует :) Давайте с Вами вспомним как выглядит сам кабель &quot;витая пара&quot;, с помощью которого мы прокладывали сети в одном из наших бесплатных уроков.</p>
<div align="center">
	<img itemprop="image" alt="Кабель витая пара - UTP" src="http://sebeadmin.ru/media/twistedpair.jpg" style="width: 350px; height: 323px; " /></div>
<p align="justify">
	&nbsp; По правильному он называется UTP кабель (Unshielded Twisted Pair - неэкранированная витая пара). То что она - витая (перекрученная) мы прекрасно видим из фото выше. Отдельные ее проводники обвиты друг с другом для улучшения помехозащищенности всего кабеля в целом.</p>
<p align="justify">
	&nbsp; Обозначение &quot;неэкранированная&quot; подразумевает, что поверх жилок отсутствует дополнительный защитный экран (оплетка) из фольги или металла. Опять же - для лучшей защищенности кабеля. А &quot;пара&quot; потому, что проводники в кабеле скручены попарно и - по цветам (бело-оранжевый - оранжевый, бело-зеленый - зеленый, бело-коричневый - коричневый, бело-синий - синий). &nbsp;</p>
<p align="justify">
	&nbsp;&nbsp;Теперь - <strong>самое важное</strong>: для обеспечения передачи данных по сети со скоростью 100 мегабит в секунду не нужно задействовать все четыре пары (восемь проводников-жилок), достаточно двух пар (четыре жилы)! Причем используются строго определенные их номера: <strong>первый</strong>, <strong>второй</strong>, <strong>третий</strong>&nbsp;и <strong>шестой</strong>&nbsp;проводок.&nbsp;</p>
