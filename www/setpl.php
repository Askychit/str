<p align="justify">
	&nbsp;&nbsp;Согласно изложенному выше, для обеспечения скорости в 100 мегабит у нас используются &quot;жилки&quot; под номером 1, 2, 3 и 6. Смотрим на рисунок выше. Это - две пары: оранжевая и зеленая.&nbsp;</p>
