<h3 align="center">Кабели и разъемы для подключения устройств</h3>
<p align="justify">В любом компьютере есть несколько небольших отверстий для подключения дополнительных устройств. Большинство из них называется <span class="bold">портами</span>. А некоторые - <span class="bold">разъемами</span>.</p>

<div class="push20">&nbsp;</div>
<p class="italic">Грань между тем, что называется портом, а что разъемом достаточно тонкая. Поэтому можно использовать любое из этих определений &ndash; это не будет большой ошибкой.</p>

<p align="justify">Такие отверстия находятся сзади, спереди или сбоку в зависимости от вида компьютера.</p>

<p class="text_c"><img src="1.jpg" /></p>

<p align="justify">В ноутбуках, например, порты расположены на боковых частях.</p>

<p class="text_c"><img src="2.jpg" /></p>

<p align="justify">В стационарных ПК большинство разъемов находится на задней стенке системного блока. А некоторые размещены еще и на передней части.</p>

<p class="text_c"><img src="3.jpg" /> <a class="fancybox" href="3_2.jpg"><img src="3_22.jpg" style="margin: 0px 0px 0px 100px;" /></a></p>

<p align="justify">Количество портов зависит от фирмы-изготовителя, &laquo;крутости&raquo; компьютера и даты его выпуска.</p>


<div class="push20">&nbsp;</div>
<h2>USB (ю-эс-би)</h2>

<p align="justify">Самый популярный разъем. Обычно таких отверстий в компьютере несколько.</p>

<p class="text_c"><img src="4.jpg" /></p>

<p align="justify">Дело в том, что USB порт универсален &ndash; через него можно подключить практически любое современное оборудование. И принтер со сканером, и мышку с клавиатурой.</p>

<p class="italic">Сейчас почти все производители снабжают свою технику специальным кабелем: один конец которого вставляется в устройство, а другой в USB разъем.</p>

<p class="text_c"><img src="5.jpg" /></p>

<p align="justify">Кроме того, этот порт служит не только для подключения компьютерного оборудования, но и для передачи данных. Например, для переноса фотографий из фотоаппарата или мобильного телефона в компьютер и наоборот.</p>

<p class="text_c"><img src="6.jpg" /></p>

<p align="justify">Ну, и, конечно же, через этот разъем подключаются USB флешки.</p>

<p class="text_c"><img src="8.jpg" /></p>

<p align="justify">Бесспорным преимуществом является скорость передачи информации. При таком подключении она достаточно высокая &ndash; данные переносятся быстро.</p>

<p align="justify">Но и это еще не все! Через USB можно подключить устройство не только, чтобы что-то с него скопировать, но и для подзарядки аккумулятора. Таким образом, мы и информацию переносим и технику подзаряжаем.</p>

<p class="text_c"><img src="9.jpg" /></p>

<p class="italic">Это касается мобильных телефонов, планшетов, электронных книг, автомобильных навигаторов, музыкальных плееров и других устройств.</p>
