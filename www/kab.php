<h2>Другие порты/разъемы компьютера</h2>

<p align="justify">Так как существует их довольно много, я расскажу про каждый буквально в двух словах.</p>

<p align="justify"><span class="bold">Аудио разъемы</span> &ndash; один для звуковых колонок или наушников, другой для микрофона. Их бывает и больше, и они служат для подключения другого аудио оборудования. На многих компьютерах они специально сделаны разных цветов, чтобы было понятнее, куда какой шнур вставлять.</p>

<p class="text_c"><img src="10.jpg" /></p>

<p align="justify"><span class="bold">Картридер</span> &ndash; в него вставляется флеш-карта памяти.</p>

<p class="text_c"><img src="11.jpg" /></p>

<p align="justify">Таким образом можно посмотреть записанные на ней данные, а также скопировать их.</p>

<p class="text_c"><img src="11_1.jpg" /></p>

<p align="justify">Порт <span class="bold">Ethernet</span> &ndash; служит для подключения к компьютерной сети (соединения компьютеров между собой) и для подключения проводного Интернета.</p>

<p class="text_c"><img src="12.jpg" /></p>

<p align="justify"><span class="bold">HDMI</span> &ndash; используется для передачи высококачественного видео. Через него можно соединить компьютер и современный телевизор, домашний кинотеатр или проектор.</p>

<p class="text_c"><img src14.jpg" /></p>

<p align="justify"><span class="bold">DisplayPort</span> &ndash; еще один порт для подключения аудио и видео техники к компьютеру.</p>

<p class="text_c"><img src="15.jpg" style="margin: 0px 20px 0px 0px;" /> или Mini DisplayPort <img src="/images/uchebnik/computer/device/ports/15_1.jpg" style="margin: 0px 0px 0px 20px;" /></p>

<p align="justify"><span class="bold">DVI</span> порт &ndash; для передачи видео с компьютера на аналоговые или цифровые устройства (мониторы, телевизоры, проекторы).</p>

<p class="text_c"><img src="16.jpg" /></p>

<p align="justify"><span class="bold">eSATA</span> &ndash; подключение внешних жестких и оптических дисков (винчестеров, CD\DVD дисководов). Часто встречаются комбинированные порты eSATA и USB. Такой разъем можно использовать еще и в качестве USB.</p>

<p class="text_c"><img src="17.jpg" /></p>

<p align="justify"><span class="bold">D-SUB (VGA)</span> &ndash; предназначен для подключения внешнего монитора или проектора. Сейчас вместо него используются другие более &laquo;продвинутые&raquo; порты (HDMI, DisplayPort).</p>

<p class="text_c"><img src="18.jpg" /></p>

<p align="justify"><span class="bold">S-Video</span> &ndash; порт для подключения аналоговых устройств (&laquo;старых&raquo; телевизоров и пр.)</p>

<p class="text_c"><img src="19.jpg" /></p>

<p align="justify"><span class="bold">IEEE 1394 (FireWire, i-Link)</span> &ndash; порт для цифровых устройств: видео и фотокамер, другой мультимедийной техники. Также через него подключаются некоторые принтеры, сканеры и внешние жесткие диски.</p>

<p class="text_c"><img src="20.jpg" /></p>

<p align="justify"><span class="bold">PS/2</span> &ndash; для подключения проводной компьютерной мышки и клавиатуры. Часто это два разноцветных разъема под шнуры таких же цветов.</p>

<p class="text_c"><img src="21.jpg" /></p>

<p align="justify"><span class="bold">RJ11</span> &ndash; разъем модема. Используется для подключения компьютера к Интернету по телефонной линии. Внешне похож на порт Ethernet, только меньше по размеру.</p>

<p class="text_c"><img src="22.jpg" /></p>

<p align="justify"><span class="bold">COM</span> &ndash; раньше использовался для подключения модемов или &laquo;древних&raquo; мышек, а также для соединения двух компьютеров. Сейчас является устаревшим и практически не используется.</p>

<p class="text_c"><img src="23.jpg" /></p>

<p align="justify"><span class="bold">LPT</span> &ndash; для подключения периферийного оборудования (принтеров, сканеров и т.д.). Устарел.</p>

<p class="text_c"><img src="24.jpg" /></p>

<p align="justify">Порт <span class="bold">PCMCIA</span> или <span class="bold">ExpressCard</span> &ndash; встречается в ноутбуках. Представляет собой длинное отверстие, закрытое пластиковой заглушкой или откидной шторкой.</p>

<p class="text_c"><img src="25.jpg" /></p>

<p align="justify">Часто используется в качестве адаптера. В него вставляется специальная карта, на которой находятся разные порты/разъемы для подключения других устройств.</p>

<p class="text_c"><img src="26.jpg" /></p>

<p align="justify">Еще через этот разъем можно &laquo;добавить&raquo; к ноутбуку ТВ-тюнер, жесткий диск, звуковую карту и массу другого оборудования. Но сейчас компьютеры выпускаются уже без этого порта &ndash; USB разъемы его вытеснили.</p>

<p class="italic">Все-таки маленький универсальный порт гораздо удобнее, чем какие-то специальные карты.</p>

<p align="justify"><span class="bold">Kensington Lock</span> (разъем для Кенсингтонского замка) &ndash; встречается у ноутбуков.</p>

<p class="text_c"><img src="27.jpg" /></p>

<p align="justify">В него вставляется специальный шнур с замком, который крепится к любому неподвижному предмету. Таким образом, компьютеры защищают от кражи в магазинах и на выставках.</p>

<p class="text_c"><img src="28.jpg" /></p>













